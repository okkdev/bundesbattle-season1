  <h2 class="mt-8 text-xl uppercase font-stencil">Format</h2>
  <p>
    We will be running 4 qualifier tournaments at the Weeklies in <a
      href="/basel"
      class="font-semibold">Basel</a
    >
    and
    <a href="/zurich" class="font-semibold">Zürich</a>.
  </p>
  <p>There are separate leaderboards for each region.</p>
  <p>The top 3 of each leaderboard will qualify for the season finale.</p>
  <p>
    Additionally, there will be 2 <a
      href="/direct-qualifiers"
      class="font-semibold">direct qualifiers</a
    > with TheReset #8 and GamingHotel 2023.
  </p>
  <p>
    Anyone is eligible to participate in the league. If the winners of the
    direct qualifiers can not attend the season finale, the spots go to the next
    higher placement from the respective tournaments If you qualify on both
    leaderboards or through a direct qualifer and at least one leaderboard, the
    direct qualifier spot takes priority, or the higher placement on the
    leaderboards and the leftover spots are distributed to the next placements.
  </p>
  <p>Points distributed at the qualifers:</p>
  <ul class="pl-6 list-disc">
    <li>1st: 8 pts</li>
    <li>2nd: 6 pts</li>
    <li>3rd: 4 pts</li>
    <li>4th: 3 pts</li>
    <li>5th: 2 pts</li>
    <li>6th: 1 pt</li>
  </ul>

  <h2 class="mt-8 text-xl uppercase font-stencil">Pricing and prizes</h2>

  <p>The regular venue and tournament fees of each respective weekly apply.</p>
  <p>
    An additional CHF 2.- are to be paid at each weekly, which will go towards
    the season finale prize pot.
  </p>
  <p>
    HardReadCH and FGC Basel provide CHF 50.- as pot bonus each for the season
    finale.
  </p>

  <h2 class="mt-8 text-xl uppercase font-stencil">Rules</h2>

  <ul class="pl-6 list-disc">
    <li>All tournaments are held in a double elimination style bracket.</li>
    <li>
      The controller may not send simultaneous opposite cardinal directions
      (SOCD) game inputs. SOCD inputs include Left+Right and Up+Down. This is
      typically enforced by adding “cleaning” firmware to the controller which
      removes one of the inputs before passing to the game. “Stock” gamepads
      (e.g. the Playstation DUALSHOCK®4) are explicitly exempt from this rule.
    </li>
    <li>
      Coaching: Max. 1 Coach per set, max. 1 minute between matches. Violating
      this rule may result in a warning and if violated again in a
      disqualification.
    </li>
    <li>Contestants may request a blind pick prior to match start.</li>
    <li>Character switch only allowed after a loss.</li>
    <li>Classic and modern control schemes are allowed.</li>
  </ul>
